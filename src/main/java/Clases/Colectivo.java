package Clases;

public class Colectivo {
    private boolean pisoDoble;

    public Colectivo() {
    }

    public Colectivo(boolean pisoDoble) {
        this.pisoDoble = pisoDoble;
    }

    public boolean isPisoDoble() {
        return pisoDoble;
    }

    public void setPisoDoble(boolean pisoDoble) {
        this.pisoDoble = pisoDoble;
    }
}
